package com.solution.s.javapractice.POJO;

public class Pojo_res {
    String name;
    int img;


    public Pojo_res(String name, int img) {
        this.name = name;
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
